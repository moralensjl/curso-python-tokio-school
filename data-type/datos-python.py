# Tipos de Datos en Python
# numericos: int
num = 2
print(f"Esto es un numero entero (int) {num}")

# float
num2 = 5.5
print(f"Esto es un numero entero (float) {num2}")

# Booleanos
verdad = True
falso = False

# Strings
nombre = "Morales"
print(f"Esto es un String {nombre}")

# Una variable es un cajon donde guardamos informacion
a = 3
print(f"El valor de a es: {a}")
b = a
print(f"El valor de b es: {b}")